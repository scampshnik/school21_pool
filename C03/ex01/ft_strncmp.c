int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int s;

	s = 0;
	while (s1[s] != '\0' && s2[s] != '\0' && s < n)
	{
		if (s1[s] != s2[s])
		{
			return (s1[s] - s2[s]);
		}
		s++;
	}
	return (s1[s] - s2[s]);
}
