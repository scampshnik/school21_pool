char	*ft_strcat(char *dest, char *src)
{
	int	s;
	int r;

	s = 0;
	r = 0;
	while (dest[s] != '\0')
	{
		s++;
	}
	while (src[r] != '\0')
	{
		dest[s] = src[r];
		s++;
		r++;
	}
	dest[s] = '\0';
	return (dest);
}
